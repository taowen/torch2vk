@dataclass(frozen=True, slots=True)
class _AliasOp:
    src: str
    dst: str


@dataclass(frozen=True, slots=True)
class _DispatchOp:
    name: str
    variant: ShaderVariant
    bindings: dict[str, str]
    push_inputs: tuple[str, ...] = ()
    q6_variant: ShaderVariant | None = None
    q8_variant: ShaderVariant | None = None


@dataclass(frozen=True, slots=True)
class _UnsupportedOp:
    name: str
    message: str


_Op = _AliasOp | _DispatchOp | _UnsupportedOp

_QuantizedTargetResolver = Callable[
    [Node, Q4KMQuantizationConfig, Mapping[str, str], str],
    ShaderVariant | None,
]


def render_dispatch_function(function_name: str, class_name: str, ops) -> str:
    return render_template(
        "dispatch_function.py.j2",
        function_name=function_name,
        class_name=class_name,
        ops=ops,
    ).rstrip("\n")


def render_dispatch_file(
    *,
    docstring: str,
    shader_package: str,
    tensor_package: str,
    shader_imports,
    tensor_imports,
    function_sources: list[str],
    extra_imports_source: str = "",
) -> str:
    return render_template(
        "dispatch_file.py.j2",
        docstring=docstring,
        shader_package=shader_package,
        tensor_package=tensor_package,
        shader_imports=shader_imports,
        tensor_imports=tensor_imports,
        extra_imports_source=extra_imports_source.rstrip("\n"),
        function_sources_source="\n\n\n".join(function_sources),
    )


def bind_dispatch_function_to_tensors(source: str) -> str:
    bound = re.sub(
        r"def (run_\w+)\(rt: RuntimeSession, tensors: (\w+)([^)]*)\) -> None:",
        r"def _\1_with_tensors(rt: RuntimeSession, tensors: \2\3) -> None:",
        source,
        count=1,
    )
    if bound == source:
        raise ValueError("generated dispatch source does not match tensor-bound signature")
    return bound


def render_model_dispatch_module(
    *,
    model_package: str,
    function_name: str,
    tensor_file: str,
    tensor_class: str,
    tensor_expr: str,
    shader_imports: Mapping[str, str],
    function_source: str,
    parameters_source: str = "",
    arguments_source: str = "",
    uses_quantized_linear_dispatch: bool = False,
) -> str:
    return render_template(
        "model_dispatch_module.py.j2",
        model_package=model_package,
        function_name=function_name,
        tensor_file=tensor_file,
        tensor_class=tensor_class,
        tensor_expr=tensor_expr,
        shader_imports=tuple(
            {"shader": shader, "const": const} for shader, const in sorted(shader_imports.items())
        ),
        function_source=function_source.rstrip("\n"),
        parameters_source=parameters_source,
        arguments_source=arguments_source,
        uses_quantized_linear_dispatch=uses_quantized_linear_dispatch,
    )


def generate_dispatch_source(
    prog: ExportedProgram,
    *,
    class_name: str = "ExportedTensors",
    function_name: str = "run_exported",
    registry: ShaderRegistry = DEFAULT_REGISTRY,
) -> str:
    graph = prog.graph_module.graph
    sig = prog.graph_signature

    node_variants = _resolve_all_variants(graph, registry)

    lines: list[str] = []
    lines.append('"""Generated by torch2vk.export. Do not edit."""')
    lines.append("")
    lines.append("from __future__ import annotations")
    lines.append("")
    lines.append("from dataclasses import dataclass")
    lines.append("")
    lines.append("from torch2vk.runtime.logical import LogicalTensor")
    lines.append("from torch2vk.runtime.session import RuntimeSession")
    lines.append("from torch2vk.runtime.shader import ShaderVariant")
    lines.append("")
    lines.append("")
    lines.extend(_generate_param_fields(sig))
    lines.append("")
    lines.append("")
    lines.extend(_generate_dataclass(graph, sig, class_name))
    lines.append("")
    lines.append("")
    lines.extend(_generate_dispatch_function(graph, class_name, function_name, node_variants))
    lines.append("")

    return "\n".join(lines)


def _resolve_all_variants(
    graph: Graph,
    registry: ShaderRegistry,
    *,
    parameter_map: Mapping[str, str] | None = None,
    quantization_config: Q4KMQuantizationConfig | None = None,
) -> dict[str, ShaderVariant]:
    variants: dict[str, ShaderVariant] = {}
    for node in graph.nodes:
        if node.op != "call_function":
            continue
        target = str(node.target)
        if target in SKIP_OPS or is_alias_op(node):
            continue
        shader = _resolve_variant(
            node,
            registry,
            parameter_map=parameter_map or {},
            quantization_config=quantization_config,
        )
        if shader is not None:
            variants[node.name] = shader
    return variants


def _resolve_variant(
    node: Node,
    registry: ShaderRegistry,
    *,
    parameter_map: Mapping[str, str],
    quantization_config: Q4KMQuantizationConfig | None,
) -> ShaderVariant | None:
    if quantization_config is not None:
        quantized = _resolve_quantized_variant(
            node,
            quantization_config=quantization_config,
            parameter_map=parameter_map,
            activation_dtype=registry.activation_dtype,
        )
        if quantized is not None:
            return quantized
    return registry.resolve(node)


def _resolve_quantized_variant(
    node: Node,
    *,
    quantization_config: Q4KMQuantizationConfig,
    parameter_map: Mapping[str, str],
    activation_dtype: str,
) -> ShaderVariant | None:
    resolver = _QUANTIZED_TARGET_RESOLVERS.get(str(node.target))
    if resolver is None:
        return None
    return resolver(node, quantization_config, parameter_map, activation_dtype)


def _resolve_quantized_linear_variant(
    node: Node,
    quantization_config: Q4KMQuantizationConfig,
    parameter_map: Mapping[str, str],
    activation_dtype: str,
) -> ShaderVariant | None:
    declaration = _quantized_weight_declaration(
        node,
        quantization_config=quantization_config,
        parameter_map=parameter_map,
        weight_arg_index=1,
    )
    if declaration is None or declaration.gguf_type is GGUFTensorType.F32:
        return None
    has_bias = len(node.args) >= 3 and isinstance(node.args[2], Node)
    if has_bias:
        if declaration.gguf_type is not GGUFTensorType.Q8_0:
            raise RuntimeError(
                f"{node.name} has quantized {declaration.gguf_type.name} linear weight with bias; "
                "only Q8_0 biased linear is supported"
            )
        return make_linear_bias_q8_0_variant(node, activation_dtype)
    checkpoint_key = _weight_checkpoint_key(node, parameter_map, 1)
    if checkpoint_key is not None and _may_need_mixed_layer_quantized_dispatch(
        checkpoint_key,
        quantization_config,
    ):
        return make_linear_nobias_q4_k_m_variant(node, activation_dtype)
    if declaration.gguf_type is GGUFTensorType.Q4_K:
        return make_linear_nobias_q4_k_m_variant(node, activation_dtype)
    if declaration.gguf_type is GGUFTensorType.Q6_K:
        return make_linear_nobias_q6_k_variant(node, activation_dtype)
    if declaration.gguf_type is GGUFTensorType.Q8_0:
        return make_linear_nobias_q8_0_variant(node, activation_dtype)
    return None


def _resolve_quantized_embedding_variant(
    node: Node,
    quantization_config: Q4KMQuantizationConfig,
    parameter_map: Mapping[str, str],
    activation_dtype: str,
) -> ShaderVariant | None:
    declaration = _quantized_weight_declaration(
        node,
        quantization_config=quantization_config,
        parameter_map=parameter_map,
        weight_arg_index=0,
    )
    if declaration is None or declaration.gguf_type is GGUFTensorType.F32:
        return None
    if declaration.gguf_type is GGUFTensorType.Q4_K:
        return make_embedding_q4_k_m_variant(node, activation_dtype)
    if declaration.gguf_type is GGUFTensorType.Q8_0:
        return make_embedding_q8_0_variant(node, activation_dtype)
    raise RuntimeError(f"{node.name} has unsupported quantized embedding weight: {declaration.gguf_type.name}")


