def _resolve_quantized_q8_only_variant(
    node: Node,
    *,
    quantization_config: Q4KMQuantizationConfig,
    parameter_map: Mapping[str, str],
    activation_dtype: str,
    weight_arg_index: int,
    factory: Callable[[Node, str], ShaderVariant | None],
) -> ShaderVariant | None:
    declaration = _quantized_weight_declaration(
        node,
        quantization_config=quantization_config,
        parameter_map=parameter_map,
        weight_arg_index=weight_arg_index,
    )
    if declaration is None or declaration.gguf_type is GGUFTensorType.F32:
        return None
    if declaration.gguf_type is not GGUFTensorType.Q8_0:
        raise RuntimeError(
            f"{node.name} has unsupported quantized convolution weight: {declaration.gguf_type.name}"
        )
    return factory(node, activation_dtype)


_QUANTIZED_TARGET_RESOLVERS: Mapping[str, _QuantizedTargetResolver] = {
    "aten.linear.default": _resolve_quantized_linear_variant,
    "aten.embedding.default": _resolve_quantized_embedding_variant,
    "aten.conv1d.default": _resolve_quantized_conv1d_variant,
    "aten.conv2d.default": _resolve_quantized_conv2d_variant,
    "aten.conv_transpose1d.default": _resolve_quantized_conv_transpose1d_variant,
}


def _quantized_weight_declaration(
    node: Node,
    *,
    quantization_config: Q4KMQuantizationConfig,
    parameter_map: Mapping[str, str],
    weight_arg_index: int,
) -> _WeightDeclaration | None:
    checkpoint_key = _weight_checkpoint_key(node, parameter_map, weight_arg_index)
    if checkpoint_key is None:
        return None
    weight_shape = node_input_shape(node, weight_arg_index)
    if not weight_shape:
        return None
    return quantization_config.declare_weight(
        checkpoint_key=checkpoint_key,
        dtype=node_input_dtype(node, weight_arg_index),
        shape=tuple(int(dim) for dim in weight_shape),
    )


def _weight_checkpoint_key(
    node: Node,
    parameter_map: Mapping[str, str],
    weight_arg_index: int,
) -> str | None:
    inputs = node_input_names(node)
    if weight_arg_index >= len(inputs):
        return None
    return parameter_map.get(inputs[weight_arg_index])


def _may_need_mixed_layer_quantized_dispatch(
    checkpoint_key: str,
    quantization_config: Q4KMQuantizationConfig,
) -> bool:
    marker = ".layers.0."
    if marker not in checkpoint_key or not quantization_config.has_q6:
        return False
    layer_prefix, suffix = checkpoint_key.split(marker, 1)
    layer_prefix = f"{layer_prefix}.layers."
    return any(
        name.startswith(layer_prefix) and name.endswith(suffix)
        for name in quantization_config.q6_tensor_names
    )


def _mixed_quantized_linear_op_names(
    graph: Graph,
    *,
    parameter_map: Mapping[str, str],
    quantization_config: Q4KMQuantizationConfig | None,
) -> frozenset[str]:
    if quantization_config is None or not quantization_config.has_q6:
        return frozenset()
    names: set[str] = set()
    for node in graph.nodes:
        if node.op != "call_function" or str(node.target) != "aten.linear.default":
            continue
        checkpoint_key = _weight_checkpoint_key(node, parameter_map, 1)
        if checkpoint_key is None:
            continue
        if _may_need_mixed_layer_quantized_dispatch(checkpoint_key, quantization_config):
            names.add(node.name)
    return frozenset(names)


def _generate_param_fields(sig: ExportGraphSignature, weight_prefix: str = "") -> list[str]:
    lines: list[str] = []
    lines.append("PARAMETER_FIELDS = {")
    for spec in sig.input_specs:
        if spec.kind in (InputKind.PARAMETER, InputKind.BUFFER):
            lines.append(f'    "{spec.arg.name}": "{weight_prefix}{spec.target}",')
    lines.append("}")
    return lines


def _parameter_map(sig: ExportGraphSignature, weight_prefix: str = "") -> dict[str, str]:
    parameter_map: dict[str, str] = {}
    for spec in sig.input_specs:
        if spec.kind in (InputKind.PARAMETER, InputKind.BUFFER):
            parameter_map[spec.arg.name] = f"{weight_prefix}{spec.target}"
    return parameter_map


def _generate_dataclass(graph: Graph, sig: ExportGraphSignature, class_name: str) -> list[str]:
    lines: list[str] = []
    lines.append("@dataclass")
    lines.append(f"class {class_name}:")

    seen: set[str] = set()
    for spec in sig.input_specs:
        name = spec.arg.name
        if name not in seen:
            lines.append(f"    {name}: LogicalTensor")
            seen.add(name)

    for node in graph.nodes:
        if node.op != "call_function":
            continue
        if str(node.target) in SKIP_OPS:
            continue
        if node.name not in seen:
            lines.append(f"    {node.name}: LogicalTensor")
            seen.add(node.name)

    return lines


def _generate_dispatch_function(
    graph: Graph,
    class_name: str,
    function_name: str,
    node_variants: dict[str, ShaderVariant],
) -> list[str]:
    lines: list[str] = []
    lines.append(
        f"def {function_name}(rt: RuntimeSession, tensors: {class_name}, shaders: dict[str, ShaderVariant]) -> None:"
    )

    for node in graph.nodes:
        if node.op != "call_function":
            continue
        target = str(node.target)
        if target in SKIP_OPS:
            continue

        if is_alias_op(node):
            continue

        shader = node_variants.get(node.name)
        if shader is None:
            message = f"unsupported exported op {target} ({node.name})"
            lines.append(f"    raise RuntimeError({message!r})")
            continue

        args = _build_shader_args(node, shader)
        lines.append(f'    shaders["{node.name}"](rt, {args})')

    return lines


def _build_shader_args(node: Node, shader: ShaderVariant) -> str:
    contract = shader.contract
    input_fields = [f for f in contract.fields if f.io_kind in (IOKind.INPUT, IOKind.INOUT)]
    output_fields = [f for f in contract.fields if f.io_kind in (IOKind.OUTPUT, IOKind.INOUT)]

    inputs = node_input_names(node)
    bindings: dict[str, str] = {}
    for i, field in enumerate(input_fields):
        if i < len(inputs):
            bindings[field.name] = f"tensors.{inputs[i]}"
    for field in output_fields:
        bindings[field.name] = f"tensors.{node.name}"

    return ", ".join(f"{k}={v}" for k, v in bindings.items())


def _generate_static_dispatch_function(
    graph: Graph,
    class_name: str,
    function_name: str,
    node_variants: dict[str, ShaderVariant],
    *,
    q6_variant_op_names: frozenset[str] = frozenset(),
) -> tuple[list[str], dict[str, str]]:
    ops = _collect_ops(graph, node_variants, q6_variant_op_names=q6_variant_op_names)
    output_names = graph_output_names(graph)
    ops = _prune_dead_ops(ops, output_names)

    unsupported = [op for op in ops if isinstance(op, _UnsupportedOp)]
    if unsupported:
        details = "\n".join(f"  - {op.message}" for op in unsupported)
        raise RuntimeError(f"{function_name} contains unsupported ops:\n{details}")

    shader_imports: dict[str, str] = {}
    lines: list[str] = []
    push_constant_params = _dispatch_push_constant_params(ops)
    lines.append(
        f"def {function_name}(rt: RuntimeSession, tensors: {class_name}"
        f"{_push_constant_parameters_source(push_constant_params)}) -> None:"
    )

    for op in ops:
        if isinstance(op, _AliasOp):
            continue
        elif isinstance(op, _DispatchOp):
            lines.append(
                _dispatch_call_source(
                    op,
                    _dispatch_args_source(op),
                    shader_imports,
                    indent="    ",
                )
            )
    if len(lines) == 1:
        lines.append("    pass")

    return lines, shader_imports


def _collect_ops(
    graph: Graph,
    node_variants: dict[str, ShaderVariant],
    *,
    q6_variant_op_names: frozenset[str] = frozenset(),
) -> list[_Op]:
    ops: list[_Op] = []
    seen_variants: dict[str, ShaderVariant] = {}

    for node in graph.nodes:
        if node.op != "call_function":
            continue
        target = str(node.target)
        if target in SKIP_OPS:
            continue

        if is_alias_op(node):
            inputs = node_input_names(node)
            src = inputs[0] if inputs else "???"
            ops.append(_AliasOp(src=src, dst=node.name))
            continue

        shader = node_variants.get(node.name)
        if shader is None:
            ops.append(
                _UnsupportedOp(
                    name=node.name, message=f"unsupported exported op {target} ({node.name})"
                )
            )
            continue

        q6_shader = _q6_variant_for_q4_k_m(node, shader) if node.name in q6_variant_op_names else None
        q8_shader = _q8_variant_for_q4_k_m(node, shader) if q6_shader is not None else None
        shader = _dedup_variant(shader, seen_variants)
        if q6_shader is not None:
            q6_shader = _dedup_variant(q6_shader, seen_variants)
        if q8_shader is not None:
            q8_shader = _dedup_variant(q8_shader, seen_variants)

        input_fields = [
            f for f in shader.contract.fields if f.io_kind in (IOKind.INPUT, IOKind.INOUT)
        ]
        output_fields = [
            f for f in shader.contract.fields if f.io_kind in (IOKind.OUTPUT, IOKind.INOUT)
        ]
        inputs = node_input_names(node)
        bindings: dict[str, str] = {}
        for i, field in enumerate(input_fields):
            if i < len(inputs):
                bindings[field.name] = f"tensors.{inputs[i]}"
        for field in output_fields:
            bindings[field.name] = f"tensors.{node.name}"
        ops.append(
            _DispatchOp(
                name=node.name,
                variant=shader,
                bindings=bindings,
                push_inputs=_dispatch_push_constant_input_names(shader, q6_shader, q8_shader),
                q6_variant=q6_shader,
                q8_variant=q8_shader,
            )
        )

    return ops


def _q6_variant_for_q4_k_m(node: Node, shader: ShaderVariant) -> ShaderVariant | None:
    base_name = _base_quantized_shader_name(shader.name)
    if base_name == "linear_nobias_q4_k_f32":
        return make_linear_nobias_q6_k_variant(node, _activation_dtype(shader), matvec=False)
    if base_name == "linear_nobias_q4_k_matvec_f32":
        return make_linear_nobias_q6_k_variant(node, _activation_dtype(shader), matvec=True)
    return None


def _dispatch_args_source(op: _DispatchOp) -> str:
    args = [f"{k}={v}" for k, v in op.bindings.items()]
    args.extend(f"{name}={name}" for name in op.push_inputs)
    return ", ".join(args)


def _dispatch_call_source(
    op: _DispatchOp,
    args: str,
    shader_imports: dict[str, str],
    *,
    indent: str,
) -> str:
    const_name = op.variant.name.upper()
    shader_imports[op.variant.name] = const_name
    args_suffix = f", {args}" if args else ""
    if op.q6_variant is None:
        return f"{indent}{const_name}(rt{args_suffix})"
    if op.q8_variant is None:
        raise RuntimeError(f"{op.name} has q6 variant but no q8 variant")
    q6_const_name = op.q6_variant.name.upper()
    q8_const_name = op.q8_variant.name.upper()
    shader_imports[op.q6_variant.name] = q6_const_name
    shader_imports[op.q8_variant.name] = q8_const_name
    return (
        f"{indent}run_quantized_linear(rt, q4={const_name}, "
        f"q6={q6_const_name}, q8={q8_const_name}{args_suffix})"
    )


def _dispatch_push_constant_input_names(
    *variants: ShaderVariant | None,
) -> tuple[str, ...]:
    names: set[str] = set()
    for variant in variants:
        if variant is None or variant.contract.push_constants is None:
            continue
        for field in variant.contract.push_constants.fields:
            if isinstance(field.value, PushConstantInput):
                names.add(field.value.name)
    return tuple(sorted(names))


def _dispatch_push_constant_params(ops: list[_Op]) -> tuple[tuple[str, str], ...]:
    types: dict[str, str] = {}
    for op in ops:
        if not isinstance(op, _DispatchOp):
            continue
        for variant in (op.variant, op.q6_variant, op.q8_variant):
            if variant is None or variant.contract.push_constants is None:
                continue
            for field in variant.contract.push_constants.fields:
                if not isinstance(field.value, PushConstantInput):
                    continue
                param_type = _push_constant_python_type(field.dtype)
                existing = types.get(field.value.name)
                if existing is not None and existing != param_type:
                    raise RuntimeError(
                        f"Push constant input {field.value.name!r} has conflicting "
                        f"types: {existing} and {param_type}"
                    )
                types[field.value.name] = param_type
    return tuple(sorted(types.items()))


def _push_constant_python_type(dtype: PushConstantType) -> str:
    if dtype is PushConstantType.FLOAT32:
        return "float"
    return "int"


def _push_constant_parameters_source(params: tuple[tuple[str, str], ...]) -> str:
    if not params:
        return ""
    return ", *, " + ", ".join(f"{name}: {param_type}" for name, param_type in params)


def _q8_variant_for_q4_k_m(node: Node, shader: ShaderVariant) -> ShaderVariant | None:
    if _base_quantized_shader_name(shader.name) in {
        "linear_nobias_q4_k_f32",
        "linear_nobias_q4_k_matvec_f32",
    }:
        return make_linear_nobias_q8_0_variant(node, _activation_dtype(shader))
    return None


def _base_quantized_shader_name(name: str) -> str:
    return name.split("_act_", 1)[0]


def _activation_dtype(shader: ShaderVariant) -> str:
    field = shader.contract.input_fields[0]
    dtype = field.contract.dtype
    if not isinstance(dtype, str):
        raise TypeError(f"{shader.name} input dtype must be concrete, got {dtype}")
    return dtype


def _dedup_variant(
    shader: ShaderVariant,
    seen_variants: dict[str, ShaderVariant],
) -> ShaderVariant:
    shader_key = shader.name
    if shader_key in seen_variants:
        if seen_variants[shader_key].contract != shader.contract:
            shader_key = f"{shader.name}_{len(seen_variants)}"
            new_contract = ShaderContract(
                class_name=shader.contract.class_name,
                shader_name=shader_key,
                fields=shader.contract.fields,
                dispatch=shader.contract.dispatch,
                push_constants=shader.contract.push_constants,
                params_buffer=shader.contract.params_buffer,
            )
            shader = ShaderVariant(
                name=shader_key,
                family=shader.family,
                contract=new_contract,
                source=shader.source,
                precompiled_spv_path=shader.precompiled_spv_path,
                specialization_constants=shader.specialization_constants,
                include_dirs=shader.include_dirs,
                compile_defines=shader.compile_defines,
                execution_requirements=shader.execution_requirements,
            )
    if shader_key not in seen_variants:
        seen_variants[shader_key] = shader
    return shader


def _prune_dead_ops(ops: list[_Op], output_names: list[str]) -> list[_Op]:
    needed = set(output_names)
    kept_reversed: list[_Op] = []
    for op in reversed(ops):
        if isinstance(op, _DispatchOp):
            if op.name not in needed:
                continue
            kept_reversed.append(op)
            for v in op.bindings.values():
                needed.add(v.removeprefix("tensors."))
        elif isinstance(op, _AliasOp):
            if op.dst not in needed:
                continue
            kept_reversed.append(op)
            needed.add(op.src)
        elif isinstance(op, _UnsupportedOp):
            if op.name not in needed:
                continue
            kept_reversed.append(op)
    return list(reversed(kept_reversed))


def generate_dispatch_function_source(
    prog: ExportedProgram,
    *,
    class_name: str = "ExportedTensors",
    function_name: str = "run_exported",
    shader_package: str = "",
    registry: ShaderRegistry = DEFAULT_REGISTRY,
    weight_prefix: str = "",
    quantization_config: Q4KMQuantizationConfig | None = None,
) -> tuple[str, dict[str, str], dict[str, ShaderVariant]]:
    """Generate dispatch function with static shader imports.

    Returns (function_source, {shader_name: CONST_NAME}, {shader_name: ShaderVariant}).
    The caller is responsible for assembling the final file with imports.
    """
    graph = prog.graph_module.graph
    parameter_map = _parameter_map(prog.graph_signature, weight_prefix)
    node_variants = _resolve_all_variants(
        graph,
        registry,
        parameter_map=parameter_map,
        quantization_config=quantization_config,
    )
    q6_variant_op_names = _mixed_quantized_linear_op_names(
        graph,
        parameter_map=parameter_map,
        quantization_config=quantization_config,
    )
    lines, shader_imports = _generate_static_dispatch_function(
        graph,
        class_name,
        function_name,
        node_variants,
        q6_variant_op_names=q6_variant_op_names,
    )
    all_ops = _collect_ops(graph, node_variants, q6_variant_op_names=q6_variant_op_names)
    output_names = graph_output_names(graph)
    live_ops = _prune_dead_ops(all_ops, output_names)
    used_variants: dict[str, ShaderVariant] = {}
    for op in live_ops:
        if isinstance(op, _DispatchOp):
            used_variants.setdefault(op.variant.name, op.variant)
            if op.q6_variant is not None:
                used_variants.setdefault(op.q6_variant.name, op.q6_variant)
            if op.q8_variant is not None:
                used_variants.setdefault(op.q8_variant.name, op.q8_variant)
    return "\n".join(lines), shader_imports, used_variants
